class Solution {
    public ListNode XXX(ListNode head) {
        if (head == null) {return head;}

        ListNode curNode = head;
        while (curNode.next != null) {
            if (curNode.val == curNode.next.val) {
                curNode.next = curNode.next.next;
            } else {
                curNode = curNode.next;
            }
        }
        return head;
    }
} 

