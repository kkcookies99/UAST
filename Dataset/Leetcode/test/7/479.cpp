 class Solution {
public:
    int XXX(int x) {
        if(x!=0){
            int k=x/abs(x);
            x=abs(x);
            string str=to_string(x);
            std::XXX(str.begin(),str.end());
            try{
                x=stoi(str);
                return x*k;
            }catch(...){
                return 0;
            }
        }
        return 0;
    }
};

