class Solution {
public:
    long wuhu;
    int XXX(int x) {
        if(x==0)
            return 0;

        string trans = to_string(abs(x));
        std::XXX(trans.begin(),trans.end());

        long tmp=1;
        x>0?wuhu=atol(trans.c_str()):wuhu=0-atol(trans.c_str());
        if(wuhu>=0-(tmp<<32) && wuhu<=(tmp<<32)-1)
            return (int)wuhu;
        else
            return 0;
    }
};

